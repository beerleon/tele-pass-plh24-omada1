    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       v = new Vehicle();
       em.persist(v);
       VehicleDialog vf = new VehicleDialog(v);
       
       vf.setVisible(true);
       thisFrame=this;
      thisFrame.setEnabled(false);
       //************
        vf.addWindowListener(new WindowListener() {
            public void windowClosed(WindowEvent arg0) {
                System.out.println("Window close event occur");
                if (v != null){
                    v.setCustId(customer1);
                    
                    if (vehicleList==null){
                        vehicleList = new ArrayList<Vehicle>();
                    }
                    
                    vehicleList.add(v);
                    int row = vehicleList.size() - 1;
        //            for (Binding b : bindingGroup.getBindings()){
        //                b.saveAndNotify();
        //            }
                    jTable2.setRowSelectionInterval(row, row);
                    jTable2.scrollRectToVisible(jTable2.getCellRect(row, 0, true));
                    thisFrame.setEnabled(true);
                }
                else{
                    em.getTransaction().rollback();
                    em.getTransaction().begin();
                }
            }
            public void windowActivated(WindowEvent arg0) {
                System.out.println("Window Activated");
            }
            public void windowClosing(WindowEvent arg0) {
                System.out.println("Window Closing");
            }
            public void windowDeactivated(WindowEvent arg0) {
                System.out.println("Window Deactivated");
            }
            public void windowDeiconified(WindowEvent arg0) {
                System.out.println("Window Deiconified");
            }
            public void windowIconified(WindowEvent arg0) {
                System.out.println("Window Iconified");
            }
            public void windowOpened(WindowEvent arg0) {
                System.out.println("Window Opened");
            }
        });
       //************
       
    
       
       
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        int s = this.jTable2.getSelectedRow();
        Vehicle v = this.customer1.getVehicleList().get(s);
        
        VehicleDialog vf = new VehicleDialog(v);
        vf.setVisible(true);
        if (v != null){
           
            em.persist(v);
            jTable2.setRowSelectionInterval(s, s);
            jTable2.scrollRectToVisible(jTable2.getCellRect(s, 0, true));
       }
        
    }//GEN-LAST:event_jButton3ActionPerformed

